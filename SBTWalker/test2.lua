function SxScriptUpdate:__init(Version, Script)
    self.LocalVersion = VERSION
    self.Host = "raw.githubusercontent.com"
    self.VersionPath = '/BoL/TCPUpdater/GetScript'..(true and '5' or '6')..'.php?script='..self:Base64Encode(self.Host..Version)..'&rand='..math.random(99999999)
    self.ScriptPath = '/BoL/TCPUpdater/GetScript'..(true and '5' or '6')..'.php?script='..self:Base64Encode(self.Host..Script)..'&rand='..math.random(99999999)
    self.SavePath = SCRIPT_PATH.._ENV.FILE_NAME
    self.CallbackUpdate = function(NewVersion,OldVersion) ScriptMessage("Updated to v"..NewVersion) end
    self.CallbackNoUpdate = function(OldVersion) ScriptMessage("No Updates Found") end
    self.CallbackNewVersion = function(NewVersion) ScriptMessage("New Version found ("..NewVersion.."). Please wait until its downloaded then F9x2") end
    self.CallbackError = function(NewVersion) ScriptMessage("Error while Downloading. Please try again.") end
    AddDrawCallback(function() self:OnDraw() end)
    self:CreateSocket(self.VersionPath)
    self.DownloadStatus = 'Connect to Server for VersionInfo'
    AddTickCallback(function() self:GetOnlineVersion() end)
end
function SxScriptUpdate:print(str)
